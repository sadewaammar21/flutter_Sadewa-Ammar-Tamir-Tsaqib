class kalkulator {
  double penjumlahan(double bil1, double bil2) {
    return bil1 + bil2;
  }

  double pengurangan(double bil1, double bil2) {
    return bil1 - bil2;
  }

  double perkalian(double bil1, double bil2) {
    return bil1 * bil2;
  }

  double pembagian(double bil1, double bil2) {
    return bil1 / bil2;
  }
}
